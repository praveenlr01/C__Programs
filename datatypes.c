#include<stdio.h>
void main()
{
    int i= 1;
    printf("The value of i =  %i\n",i);
    float j=2;
    printf("The value of j = %f\n",j);
    char k='c';
    printf("The value of k = %c\n",k);
    double l=2.3445;
    printf("The value of l = %lf\n",l);
    unsigned int m=10;
    printf("The value of i = %u\n",m);
}
