#include<stdio.h>
void main()
{
    int i= 1;
    printf("The value of i =  %i\n",i);
    float j=2;
    float jj=2.03;
    printf("The value of j = %f \t The value of jj = %f \n",j,jj);
    char k='c';
    printf("The value of k = %c\n",k);
    double l=2.344e5;
    double m=2.0944;
    printf("The value of l = %lf \t The value of m = %lf\n",l,m);
    unsigned int n=0;
    printf("The value of i = %u\n",n);
}
